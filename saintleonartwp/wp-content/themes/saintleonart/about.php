			<section class="presentation">
				<h3  class="presentation__heading3 heading3" aria-level="3" role="heading">Qu'est ce que Saint Léon'Art ?</h3>
				<div class="presentation__content">
					<p class="presentation__content--txt">
						<span>Saint Léon'Art,</span> c'est un vaste parcours artistique citoyen, mis sur pied par les artistes eux-mêmes et les associations du quartier Saint-Léonard.
					</p>
					<p class="presentation__content--txt">
						Ce site web vous présente la programmation de l'édition 2018 et permet aux artistes de s'inscrire à l'évènement via la page contact.
					</p>
				</div>
			</section>
			<section class="historique">
				<h3  class="historique__heading3 heading3" aria-level="3" role="heading">Le quartier & le festival</h3>
				<div class="historique__content">
					<p class="historique__content--txt">
						Ancienne zone industrielle, ce quartier, qui regroupe plus de 200 artistes et artisants, toutes disciplines confondues, dispose d'une richesse culturelle indéniable et d'un dynamisme important qu'il veut valoriser et partager avec ses habitants mais aussi avec les Liégeois de tout horizon. Saint-Léon'Art vise à favoriser la rencontre entre la population du quartier, largement multiculturelle, et l'art sous toutes ses formes.
					</p>
					<p class="historique__content--txt">
						Cette initiative a également pour objectif de rendre l'art et la culture accessible à tous, toutes origines et toutes classes sociales confondues, en offrant un accès libre et gratuit à l'évènement. Durant le week-end, une foule d'animations sont prévues pour les visiteurs :
						<ul class="list">
							<li class="list__elt">
								<p class="list__elt--txt">
									<span>Découverte d'un parcours artistique unique :</span> les visiteurs sont invités à déambuler dans le quartier au gré de leurs centres d'intérêts et de leurs envies pour découvrire les multiples lieux d'exposition dans des lieux emblématiques du quartier.
								</p>
							</li>
							<li class="list__elt">
								<p class="list__elt--txt">
									<span>Des ouvertures d'ateliers :</span> des artistes accueillent les visiteurs chez eux et leur font découvrir leur univers.
								</p>
							</li>
							<li class="list__elt">
								<p class="list__elt--txt">
									<span>Des œuvres dans l'espace urbain :</span> les visiteurs sont invités à découvrir différentes œuvres et/ou animations, se déroulant sur l'espace public, tout au long du parcours.
								</p>
							</li>
							<li class="list__elt">
								<p class="list__elt--txt">
									<span>Une ambiance musicale conviviale :</span> concerts, spectacles, soirées, ... vous seront proposé tout au long de l'exposition Saint-Léon'Art
								</p>
							</li>
						</ul>
					</p>
				</div>
